export function getRasiByDate(month: number, day: number): Rasi {
  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return 'mesa';
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return 'prusop';
  if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) return 'metuna';
  if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) return 'karakada';
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return 'singha';
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return 'kanya';
  if ((month === 9 && day >= 23) || (month === 10 && day <= 22)) return 'tula';
  if ((month === 10 && day >= 23) || (month === 11 && day <= 21)) return 'prusacik';
  if ((month === 11 && day >= 22) || (month === 12 && day <= 21)) return 'tanu';
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return 'makorn';
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return 'kumpapan';
  return 'meena';
}

