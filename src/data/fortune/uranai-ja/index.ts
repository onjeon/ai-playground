export function getSeizaByDate(month: number, day: number): Seiza {
  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return 'ohitsuji';
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return 'oushi';
  if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) return 'futago';
  if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) return 'kani';
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return 'shishi';
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return 'otome';
  if ((month === 9 && day >= 23) || (month === 10 && day <= 22)) return 'tenbin';
  if ((month === 10 && day >= 23) || (month === 11 && day <= 21)) return 'sasori';
  if ((month === 11 && day >= 22) || (month === 12 && day <= 21)) return 'ite';
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return 'yagi';
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return 'mizugame';
  return 'uo';
}

