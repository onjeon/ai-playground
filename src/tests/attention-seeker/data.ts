export function calculateResult(answers: number[]): AttentionSeekerResult {
  const totalScore = answers.reduce((sum, answerIndex, questionIndex) => {
    const question = questions[questionIndex];
    if (question && question.options[answerIndex]) {
      return sum + question.options[answerIndex].score;
    }
    return sum;
  }, 0);

  // ìµœëŒ€ ì ìˆ˜: 48ì  (12ë¬¸í•­ x 4ì )
  const percentage = (totalScore / 48) * 100;

  let resultType: AttentionSeekerType;
  if (percentage <= 25) {
    resultType = 'introvert';
  } else if (percentage <= 40) {
    resultType = 'balanced';
  } else if (percentage <= 55) {
    resultType = 'social';
  } else if (percentage <= 75) {
    resultType = 'spotlight';
  } else {
    resultType = 'maincharacter';
  }

  return results[resultType];
}
