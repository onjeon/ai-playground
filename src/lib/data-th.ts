// 유틸리티 함수
export function getTestBySlugTh(slug: string): TestTh | undefined {
  return testsTh.find((test) => test.slug === slug);
}

export function getTestsByCategoryTh(categoryId: string): TestTh[] {
  return testsTh.filter((test) => test.categoryId === categoryId);
}

export function getCategoryByIdTh(categoryId: string): CategoryTh | undefined {
  return categoriesTh.find((category) => category.id === categoryId);
}

export function getAllTestSlugsTh(): string[] {
  return testsTh.map((test) => test.slug);
}

export function getPopularTestsTh(limit: number = 10): TestTh[] {
  return [...testsTh]
    .sort((a, b) => b.participantCount - a.participantCount)
    .slice(0, limit);
}

export function getRecentTestsTh(limit: number = 10): TestTh[] {
  return [...testsTh]
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
}

export function searchTestsTh(query: string): TestTh[] {
  const lowerQuery = query.toLowerCase();
  return testsTh.filter(
    (test) =>
      test.title.toLowerCase().includes(lowerQuery) ||
      test.description.toLowerCase().includes(lowerQuery) ||
      test.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
}
