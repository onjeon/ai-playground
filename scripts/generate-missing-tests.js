function generateTestFile(testData, testId) {
  const { title, comment, types, questions, results } = testData;
  
  let questionsCode = questions.map((q, idx) => {
    const optionsCode = q.options.map((opt, optIdx) => 
      `      { text: '${opt}', type: '${types[optIdx]}' }`
    ).join(',\n');
    
    return `  {
    id: ${idx + 1},
    text: '${q.text}',
    options: [
${optionsCode}
    ],
  }`;
  }).join(',\n');

  let resultsCode = Object.entries(results).map(([key, val]) => {
    const charsArray = val.chars.map(c => `'${c}'`).join(', ');
    return `  ${key}: {
    type: '${key}',
    title: '${val.title}',
    emoji: '${val.emoji}',
    percentage: '${title}',
    description: '${val.desc}',
    characteristics: [${charsArray}],
    advice: 'ã‚ãªãŸã‚‰ã—ã•ã‚’å¤§åˆ‡ã«ï¼',
  }`;
  }).join(',\n');

  const typesInit = types.map(t => `${t}: 0`).join(', ');

  return `// ${title} - ${comment}
export const questions = [
${questionsCode},
];

export const results: Record<string, {
  type: string;
  title: string;
  emoji: string;
  percentage: string;
  description: string;
  characteristics: string[];
  advice: string;
}> = {
${resultsCode},
};

export function calculateResult(answers: number[]): typeof results[keyof typeof results] {
  const scores: Record<string, number> = { ${typesInit} };

  answers.forEach((answerIndex, questionIndex) => {
    const question = questions[questionIndex];
    if (question && question.options[answerIndex]) {
      const type = question.options[answerIndex].type;
      scores[type]++;
    }
  });

  const resultType = Object.entries(scores).reduce((a, b) => (b[1] > a[1] ? b : a))[0];
  return results[resultType];
}
`;
}

